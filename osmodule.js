const os = require('os');

console.log(os.freemem());      // gives the free memory of computer
console.log(os.homedir());      // gives the home directory
console.log(os.hostname());     // gives the hostname of computer
console.log(os.platform());     // gives the platform of OS
console.log(os.release());      // gives the release version of OS
console.log(os.totalmem());     // gives the total memory of computer
console.log(os.type());         // gives the type of OS
